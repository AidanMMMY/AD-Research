def sw_code_for(sw_l1_name: str | None) -> str | None:
    """Return the 申万一级 index code for a SW L1 name (or None)."""
    if not sw_l1_name:
        return None
    return SW_L1.get(sw_l1_name)


def map_csrc_to_sw(csrc_industry: str | None) -> tuple[str | None, str | None]:
    """Map a Tushare/CSRC industry name to (SW L1 name, SW L1 code).

    Returns (None, None) when the industry is unrecognised or None.
    """
    if not csrc_industry:
        return None, None
    name = CSRC_TO_SW_L1.get(csrc_industry)
    if name is None:
        name = CSRC_TO_SW_L1.get(csrc_industry.strip())
    if name is None:
        return None, None
    return name, SW_L1.get(name)
